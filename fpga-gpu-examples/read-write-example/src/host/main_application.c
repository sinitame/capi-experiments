/**
 * SNAP GPU Example
 *
 * Demonstration how to use SNAP with a GPU.
 * Data is generated by the FPGA (vector of size N) and GPU get this data to
 * perform an addition of this vector with itself before sending back the result
 * in the HOST memory.
 * 
 */

